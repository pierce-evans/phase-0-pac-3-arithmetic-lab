function add(a, b) {
return a + b;
}
function subtract(a, b) {
return a - b;
}
function multiply (a, b) {
return a * b;
}
function divide(a, b) {
return a / b;
}
function increment(a) {
    return a = a + 1;
}
function decrement(b) {
    return b = b - 1;
}
function makeInt(a) {
    return parseInt (a.toString(), 10);
}
function preserveDecimal(a) {
    return parseFloat (a);
}